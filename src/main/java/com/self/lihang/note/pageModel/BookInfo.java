package com.self.lihang.note.pageModel;

public class BookInfo {
    private Double rate;
    private Integer followPeople;
    private Integer noteNumber;
    private Integer commentNumber;

    public Double getRate() {
        return rate;
    }

    public void setRate(Double rate) {
        this.rate = rate;
    }

    public Integer getFollowPeople() {
        return followPeople;
    }

    public void setFollowPeople(Integer followPeople) {
        this.followPeople = followPeople;
    }

    public Integer getNoteNumber() {
        return noteNumber;
    }

    public void setNoteNumber(Integer noteNumber) {
        this.noteNumber = noteNumber;
    }

    public Integer getCommentNumber() {
        return commentNumber;
    }

    public void setCommentNumber(Integer commentNumber) {
        this.commentNumber = commentNumber;
    }
}
